$(".onlyNumeric").on("keypress keyup blur", function(event) {
    $(this).val(
        $(this)
        .val()
        .replace(/[^\d].+/, "")
        .slice(0, 10)
    );
    if (event.which < 48 || event.which > 57) {
        event.preventDefault();
    }
});